// InstallOpenDKIM installs OpenDKIM and generates keys for the domain.
func InstallOpenDKIM(ctx context.Context, domain string) (string, error) {
	result, err := RunCommandLong(ctx, 10*time.Minute, "bash", "-c",
		"DEBIAN_FRONTEND=noninteractive apt-get install -y opendkim opendkim-tools")
	if err != nil {
		return "", fmt.Errorf("failed to install opendkim: %w", err)
	}

	// Create directories
	os.MkdirAll("/etc/opendkim/keys/"+domain, 0750)

	// Generate DKIM key
	RunCommand(ctx, "opendkim-genkey", "-b", "2048", "-d", domain, "-D", "/etc/opendkim/keys/"+domain, "-s", "mail", "-v")
	RunCommand(ctx, "chown", "-R", "opendkim:opendkim", "/etc/opendkim")

	// Write opendkim.conf
	opendkimConf := fmt.Sprintf(`# OpenDKIM config - generated by ServerPanel
AutoRestart             Yes
AutoRestartRate         10/1h
Syslog                  yes
SyslogSuccess           Yes
LogWhy                  Yes
Canonicalization        relaxed/simple
ExternalIgnoreList      refile:/etc/opendkim/TrustedHosts
InternalHosts           refile:/etc/opendkim/TrustedHosts
KeyTable                refile:/etc/opendkim/KeyTable
SigningTable            refile:/etc/opendkim/SigningTable
Mode                    sv
PidFile                 /var/run/opendkim/opendkim.pid
SignatureAlgorithm      rsa-sha256
UserID                  opendkim:opendkim
Socket                  inet:12301@localhost
`)
	os.WriteFile("/etc/opendkim.conf", []byte(opendkimConf), 0644)

	// TrustedHosts
	trustedHosts := fmt.Sprintf("127.0.0.1\nlocalhost\n%s\n*.%s\n", domain, domain)
	os.WriteFile("/etc/opendkim/TrustedHosts", []byte(trustedHosts), 0644)

	// KeyTable
	keyTable := fmt.Sprintf("mail._domainkey.%s %s:mail:/etc/opendkim/keys/%s/mail.private\n", domain, domain, domain)
	os.WriteFile("/etc/opendkim/KeyTable", []byte(keyTable), 0644)

	// SigningTable
	signingTable := fmt.Sprintf("*@%s mail._domainkey.%s\n", domain, domain)
	os.WriteFile("/etc/opendkim/SigningTable", []byte(signingTable), 0644)

	// Add milter to Postfix
	RunCommand(ctx, "bash", "-c", `postconf -e "milter_protocol = 6"`)
	RunCommand(ctx, "bash", "-c", `postconf -e "milter_default_action = accept"`)
	RunCommand(ctx, "bash", "-c", `postconf -e "smtpd_milters = inet:localhost:12301"`)
	RunCommand(ctx, "bash", "-c", `postconf -e "non_smtpd_milters = inet:localhost:12301"`)

	ServiceAction(ctx, "opendkim", "enable")
	ServiceAction(ctx, "opendkim", "start")

	return "OpenDKIM installed with keys generated\n" + result.Output, nil
}

// InstallClamAV installs ClamAV antivirus daemon.
func InstallClamAV(ctx context.Context) (string, error) {
	result, err := RunCommandLong(ctx, 15*time.Minute, "bash", "-c",
		"DEBIAN_FRONTEND=noninteractive apt-get install -y clamav clamav-daemon")
	if err != nil {
		return "", fmt.Errorf("failed to install clamav: %w", err)
	}

	// Stop freshclam if running (to avoid lock conflict), update definitions, restart
	RunCommand(ctx, "systemctl", "stop", "clamav-freshclam")
	RunCommandLong(ctx, 10*time.Minute, "freshclam")
	ServiceAction(ctx, "clamav-freshclam", "enable")
	ServiceAction(ctx, "clamav-freshclam", "start")
	ServiceAction(ctx, "clamav-daemon", "enable")
	ServiceAction(ctx, "clamav-daemon", "start")

	return "ClamAV installed and virus definitions updated\n" + result.Output, nil
}

// OpenEmailFirewallPorts opens SMTP, submission, SMTPS, IMAPS, POP3S ports via UFW.
func OpenEmailFirewallPorts(ctx context.Context) (string, error) {
	ports := []string{"25/tcp", "465/tcp", "587/tcp", "993/tcp", "995/tcp"}
	var outputs []string
	for _, p := range ports {
		result, _ := RunCommand(ctx, "ufw", "allow", p)
		if result != nil {
			outputs = append(outputs, result.Output)
		}
	}
	return fmt.Sprintf("Opened firewall ports: %s\n%s", strings.Join(ports, ", "), strings.Join(outputs, "\n")), nil
}

// EnableEmailServices enables and starts all configured email services.
func EnableEmailServices(ctx context.Context, spamassassin, opendkim, clamav bool) (string, error) {
	services := []string{"postfix", "dovecot"}
	if spamassassin {
		services = append(services, "spamassassin")
	}
	if opendkim {
		services = append(services, "opendkim")
	}
	if clamav {
		services = append(services, "clamav-daemon", "clamav-freshclam")
	}

	var outputs []string
	for _, svc := range services {
		ServiceAction(ctx, svc, "enable")
		ServiceAction(ctx, svc, "restart")
		outputs = append(outputs, fmt.Sprintf("  %s: enabled and restarted", svc))
	}

	return fmt.Sprintf("Services started:\n%s", strings.Join(outputs, "\n")), nil
}

// VerifyEmailServices checks that all expected services are running.
func VerifyEmailServices(ctx context.Context, spamassassin, opendkim, clamav bool) (string, error) {
	services := []string{"postfix", "dovecot"}
	if spamassassin {
		services = append(services, "spamassassin")
	}
	if opendkim {
		services = append(services, "opendkim")
	}
	if clamav {
		services = append(services, "clamav-daemon")
	}

	var outputs []string
	var failedServices []string
	for _, svc := range services {
		result, err := RunCommand(ctx, "systemctl", "is-active", svc)
		status := "inactive"
		if err == nil && result != nil {
			status = strings.TrimSpace(result.Output)
		}
		outputs = append(outputs, fmt.Sprintf("  %s: %s", svc, status))
		if status != "active" {
			failedServices = append(failedServices, svc)
		}
	}

	output := strings.Join(outputs, "\n")
	if len(failedServices) > 0 {
		return output, fmt.Errorf("services not running: %s", strings.Join(failedServices, ", "))
	}

	return "All services verified running:\n" + output, nil
}

// CheckEmailServiceStatus returns whether a specific service is installed, running, and its version.
func CheckEmailServiceStatus(ctx context.Context, service, pkg string) (installed bool, running bool, version string) {
	// Check if running
	result, err := RunCommand(ctx, "systemctl", "is-active", service)
	if err == nil && result != nil && strings.TrimSpace(result.Output) == "active" {
		running = true
	}

	// Check if installed and get version
	result, err = RunCommand(ctx, "dpkg-query", "-W", "-f=${Version}", pkg)
	if err == nil && result != nil && result.ExitCode == 0 {
		installed = true
		version = strings.TrimSpace(result.Output)
	}

	return
}

func generatePostfixMainCf(hostname, domain string) string {
	return fmt.Sprintf(`# Postfix main.cf - generated by ServerPanel
smtpd_banner = $myhostname ESMTP
biff = no
append_dot_mydomain = no
readme_directory = no
compatibility_level = 3.6

myhostname = %s
mydomain = %s
myorigin = $mydomain
mydestination = localhost
mynetworks = 127.0.0.0/8 [::ffff:127.0.0.0]/104 [::1]/128
inet_interfaces = all
inet_protocols = all

# Virtual mailbox settings
virtual_mailbox_domains = hash:/etc/postfix/virtual_mailbox_domains
virtual_mailbox_maps = hash:/etc/postfix/virtual_mailbox_maps
virtual_mailbox_base = /var/mail/vhosts
virtual_uid_maps = static:5000
virtual_gid_maps = static:5000
virtual_transport = lmtp:unix:private/dovecot-lmtp

# TLS settings
smtpd_tls_security_level = may
smtpd_tls_cert_file = /etc/ssl/certs/ssl-cert-snakeoil.pem
smtpd_tls_key_file = /etc/ssl/private/ssl-cert-snakeoil.key
smtpd_tls_protocols = !SSLv2, !SSLv3, !TLSv1, !TLSv1.1
smtp_tls_security_level = may
smtp_tls_protocols = !SSLv2, !SSLv3, !TLSv1, !TLSv1.1

# SASL settings (Dovecot auth)
smtpd_sasl_type = dovecot
smtpd_sasl_path = private/auth
smtpd_sasl_auth_enable = yes
smtpd_recipient_restrictions = permit_sasl_authenticated, permit_mynetworks, reject_unauth_destination

# Limits
message_size_limit = 52428800
mailbox_size_limit = 0
`, hostname, domain)
}
