// RunCommandLong runs a command with a custom timeout for long-running operations.
func RunCommandLong(ctx context.Context, timeout time.Duration, name string, args ...string) (*CommandResult, error) {
	ctx, cancel := context.WithTimeout(ctx, timeout)
	defer cancel()

	return RunCommand(ctx, name, args...)
}

// UpdatePackageLists runs apt-get update.
func UpdatePackageLists(ctx context.Context) (string, error) {
	result, err := RunCommandLong(ctx, 10*time.Minute, "apt-get", "update", "-y")
	if err != nil {
		return "", fmt.Errorf("apt-get update failed: %w", err)
	}
	return result.Output, nil
}

// InstallPostfix installs and configures Postfix MTA.
func InstallPostfix(ctx context.Context, hostname, domain string) (string, error) {
	// Pre-seed debconf to avoid interactive prompts
	preseed := fmt.Sprintf(
		"postfix postfix/main_mailer_type select Internet Site\npostfix postfix/mailname string %s\n",
		hostname,
	)
	if err := os.WriteFile("/tmp/postfix-preseed.txt", []byte(preseed), 0644); err != nil {
		return "", fmt.Errorf("failed to write preseed: %w", err)
	}
	RunCommand(ctx, "debconf-set-selections", "/tmp/postfix-preseed.txt")

	// Install postfix
	result, err := RunCommandLong(ctx, 10*time.Minute, "bash", "-c",
		"DEBIAN_FRONTEND=noninteractive apt-get install -y postfix")
	if err != nil {
		return "", fmt.Errorf("failed to install postfix: %w", err)
	}

	// Create vmail user and directories
	RunCommand(ctx, "groupadd", "-g", "5000", "vmail")
	RunCommand(ctx, "useradd", "-g", "vmail", "-u", "5000", "vmail", "-d", "/var/mail/vhosts", "-s", "/usr/sbin/nologin")
	os.MkdirAll("/var/mail/vhosts/"+domain, 0770)
	RunCommand(ctx, "chown", "-R", "vmail:vmail", "/var/mail/vhosts")

	// Write main.cf
	mainCf := generatePostfixMainCf(hostname, domain)
	if err := os.WriteFile("/etc/postfix/main.cf", []byte(mainCf), 0644); err != nil {
		return "", fmt.Errorf("failed to write main.cf: %w", err)
	}

	// Create virtual mailbox map files
	os.WriteFile("/etc/postfix/virtual_mailbox_domains", []byte(domain+"\n"), 0644)
	os.WriteFile("/etc/postfix/virtual_mailbox_maps", []byte(""), 0644)
	RunCommand(ctx, "postmap", "/etc/postfix/virtual_mailbox_domains")
	RunCommand(ctx, "postmap", "/etc/postfix/virtual_mailbox_maps")

	return "Postfix installed and configured\n" + result.Output, nil
}

