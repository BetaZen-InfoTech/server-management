// ConfigurePostfixMasterCf configures submission and smtps ports in master.cf.
func ConfigurePostfixMasterCf(ctx context.Context) (string, error) {
	masterCfAdditions := `
# Submission port (587)
submission inet n       -       y       -       -       smtpd
  -o syslog_name=postfix/submission
  -o smtpd_tls_security_level=encrypt
  -o smtpd_sasl_auth_enable=yes
  -o smtpd_tls_auth_only=yes
  -o smtpd_reject_unlisted_recipient=no
  -o smtpd_recipient_restrictions=permit_sasl_authenticated,reject
  -o milter_macro_daemon_name=ORIGINATING

# SMTPS port (465)
smtps     inet  n       -       y       -       -       smtpd
  -o syslog_name=postfix/smtps
  -o smtpd_tls_wrappermode=yes
  -o smtpd_sasl_auth_enable=yes
  -o smtpd_reject_unlisted_recipient=no
  -o smtpd_recipient_restrictions=permit_sasl_authenticated,reject
  -o milter_macro_daemon_name=ORIGINATING
`
	// Read existing master.cf
	existing, err := os.ReadFile("/etc/postfix/master.cf")
	if err != nil {
		return "", fmt.Errorf("failed to read master.cf: %w", err)
	}

	// Append submission/smtps if not already present
	content := string(existing)
	if !strings.Contains(content, "submission inet") {
		content += masterCfAdditions
		if err := os.WriteFile("/etc/postfix/master.cf", []byte(content), 0644); err != nil {
			return "", fmt.Errorf("failed to write master.cf: %w", err)
		}
	}

	return "Postfix master.cf configured with submission/smtps ports", nil
}

// ConfigureDovecot configures Dovecot for virtual mailbox authentication and LMTP.
func ConfigureDovecot(ctx context.Context, domain string) (string, error) {
	// 10-auth.conf - disable system auth, enable passwd-file
	authConf := `# Dovecot authentication config - generated by ServerPanel
disable_plaintext_auth = yes
auth_mechanisms = plain login

!include auth-passwdfile.conf.ext
`
	os.WriteFile("/etc/dovecot/conf.d/10-auth.conf", []byte(authConf), 0644)

	// auth-passwdfile.conf.ext - password file auth
	passwdFileConf := `passdb {
  driver = passwd-file
  args = scheme=BLF-CRYPT username_format=%u /etc/dovecot/users
}

userdb {
  driver = static
  args = uid=vmail gid=vmail home=/var/mail/vhosts/%d/%n
}
`
	os.WriteFile("/etc/dovecot/conf.d/auth-passwdfile.conf.ext", []byte(passwdFileConf), 0644)

	// Create empty users file
	os.WriteFile("/etc/dovecot/users", []byte(""), 0644)
	RunCommand(ctx, "chown", "dovecot:dovecot", "/etc/dovecot/users")
	RunCommand(ctx, "chmod", "640", "/etc/dovecot/users")

	// 10-mail.conf - virtual mailbox location
	mailConf := `# Dovecot mail config - generated by ServerPanel
mail_location = maildir:/var/mail/vhosts/%d/%n/Maildir
mail_privileged_group = vmail

namespace inbox {
  inbox = yes
}
`
	os.WriteFile("/etc/dovecot/conf.d/10-mail.conf", []byte(mailConf), 0644)

	// 10-master.conf - LMTP socket and auth socket for Postfix
	masterConf := `# Dovecot master config - generated by ServerPanel
service imap-login {
  inet_listener imap {
    port = 0
  }
  inet_listener imaps {
    port = 993
    ssl = yes
  }
}

service pop3-login {
  inet_listener pop3 {
    port = 0
  }
  inet_listener pop3s {
    port = 995
    ssl = yes
  }
}

service lmtp {
  unix_listener /var/spool/postfix/private/dovecot-lmtp {
    mode = 0600
    user = postfix
    group = postfix
  }
}

service auth {
  unix_listener /var/spool/postfix/private/auth {
    mode = 0660
    user = postfix
    group = postfix
  }
  unix_listener auth-userdb {
    mode = 0600
    user = vmail
  }
  user = dovecot
}

service auth-worker {
  user = vmail
}
`
	os.WriteFile("/etc/dovecot/conf.d/10-master.conf", []byte(masterConf), 0644)

	// 10-ssl.conf - SSL settings
	sslConf := `# Dovecot SSL config - generated by ServerPanel
ssl = required
ssl_cert = </etc/ssl/certs/ssl-cert-snakeoil.pem
ssl_key = </etc/ssl/private/ssl-cert-snakeoil.key
ssl_min_protocol = TLSv1.2
ssl_prefer_server_ciphers = yes
`
	os.WriteFile("/etc/dovecot/conf.d/10-ssl.conf", []byte(sslConf), 0644)

	return "Dovecot configured for virtual mailboxes with LMTP and auth sockets", nil
}

